        private void Test<T>(Func<T> stringAction, Func<T> enumerableAction)
        {
            var actual = GetValueOrException(stringAction);
            var expected = GetValueOrException(enumerableAction);

            if (actual == null || expected == null)
            {
                Expect(actual, EqualTo(expected));
                return;
            }

            if (actual is Exception || expected is Exception)
            {
                var actualType = actual.GetType().FullName;
                var expectedType = expected.GetType().FullName;

                Expect(actualType, EqualTo(expectedType),
                       "Exceptions were generated from at least one extension method and the types didn't match.");
            }
            else
            {
                Expect(actual, EqualTo(expected), "StringExtensions doesn't match Enumerable");
            }

        }

        private static object GetValueOrException<T>(Func<T> action)
        {
            try
            {
                return action();
            }
            catch (Exception ex)
            {
                return ex;
            }
        }

        public static bool CharIsEven(char c)
        {
            return c % 2 == 0;
        }

        private class CharComparer : IEqualityComparer<char>
        {
            private readonly StringComparer _stringComparer;

            public CharComparer(StringComparer stringComparer)
            {
                _stringComparer = stringComparer;
            }
            public bool Equals(char x, char y)
            {
                return _stringComparer.Equals(x.ToString(), y.ToString());
            }

            public int GetHashCode(char obj)
            {
                return _stringComparer.GetHashCode(obj.ToString());
            }
        }
